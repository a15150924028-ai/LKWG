const fs = require("fs");
const path = require("path");

const packageRoot = path.resolve(__dirname, "..");
const repositoryRoot = path.resolve(packageRoot, "..");
const sourcePath = path.join(repositoryRoot, "data", "local-bundle.json");
const outputDir = path.join(packageRoot, "miniprogram", "data");
const outputPaths = {
  meta: path.join(outputDir, "local-meta.js"),
  monsters: path.join(outputDir, "local-monsters.js"),
  monsterSummaries: path.join(outputDir, "monster-summaries.js"),
  skills: path.join(outputDir, "local-skills.js"),
  passives: path.join(outputDir, "local-passives.js")
};
const allowedTypes = new Set([
  "grass", "water", "fire", "electric", "poison", "fantasy", "ice",
  "fighting", "cute", "light", "dragon", "mechanical", "ghost", "demon",
  "bug", "normal", "wing", "ground"
]);
const statKeys = ["hp", "atk", "defense", "spa", "spd", "spe"];

function fail(message) {
  throw new Error(`Mini Program data validation failed: ${message}`);
}

function assertUniqueId(record, prefix, ids, label) {
  if (!record || typeof record !== "object") fail(`${label} must be an object`);
  if (!new RegExp(`^${prefix}-[^\\s]+$`).test(String(record.id || ""))) {
    fail(`${label}.id must start with ${prefix}-`);
  }
  if (ids.has(record.id)) fail(`${label}.id is duplicated: ${record.id}`);
  ids.add(record.id);
  if (!String(record.name || "").trim()) fail(`${label}.name is required`);
}

function validateBundle(bundle) {
  if (!bundle || typeof bundle !== "object") fail("root must be an object");
  if (bundle.schemaVersion !== 1) fail("schemaVersion must be 1");
  for (const key of ["monsters", "skills", "passives"]) {
    if (!Array.isArray(bundle[key])) fail(`${key} must be an array`);
  }

  const skillIds = new Set();
  const passiveIds = new Set();
  const monsterIds = new Set();

  bundle.skills.forEach((skill, index) => {
    const label = `skills[${index}]`;
    assertUniqueId(skill, "skill", skillIds, label);
    if (!allowedTypes.has(skill.type)) fail(`${label}.type is invalid: ${skill.type}`);
    if (!String(skill.category || skill.mode || "").trim()) {
      fail(`${label} requires category or mode`);
    }
    if (
      !Object.prototype.hasOwnProperty.call(skill, "power")
      || (skill.power !== null && !Number.isFinite(Number(skill.power)))
    ) {
      fail(`${label}.power must be numeric or null`);
    }
    if (
      !Object.prototype.hasOwnProperty.call(skill, "pp")
      && !Object.prototype.hasOwnProperty.call(skill, "energyCost")
    ) {
      fail(`${label} requires pp or energyCost`);
    }
    if (typeof skill.description !== "string") fail(`${label}.description must be text`);
  });

  bundle.passives.forEach((passive, index) => {
    const label = `passives[${index}]`;
    assertUniqueId(passive, "passive", passiveIds, label);
    if (typeof passive.description !== "string") {
      fail(`${label}.description must be text`);
    }
  });

  bundle.monsters.forEach((monster, index) => {
    const label = `monsters[${index}]`;
    assertUniqueId(monster, "monster", monsterIds, label);
    if (
      !Array.isArray(monster.types)
      || monster.types.length === 0
      || monster.types.some((type) => !allowedTypes.has(type))
    ) {
      fail(`${label}.types must contain valid types`);
    }
    if (!Array.isArray(monster.skillIds)) fail(`${label}.skillIds must be an array`);
    if (!Array.isArray(monster.passiveIds)) fail(`${label}.passiveIds must be an array`);
    if (!monster.stats || typeof monster.stats !== "object") fail(`${label}.stats is required`);
    for (const key of statKeys) {
      if (!Number.isFinite(Number(monster.stats[key]))) {
        fail(`${label}.stats.${key} must be numeric`);
      }
    }
  });

  for (const monster of bundle.monsters) {
    for (const skillId of monster.skillIds) {
      if (!skillIds.has(skillId)) fail(`${monster.name} references missing skill ${skillId}`);
    }
    for (const passiveId of monster.passiveIds) {
      if (!passiveIds.has(passiveId)) {
        fail(`${monster.name} references missing passive ${passiveId}`);
      }
    }
  }

  return bundle;
}

function generatedSource(value) {
  return [
    "// Generated by scripts/sync-miniprogram-data.js. Do not edit directly.",
    `module.exports = Object.freeze(${JSON.stringify(value, null, 2)});`,
    ""
  ].join("\n");
}

function generatedOutputs(bundle) {
  const monsterSummaries = bundle.monsters.map((monster) => ({
    id: monster.id,
    name: monster.name,
    aliases: monster.aliases || [],
    types: monster.types,
    passiveIds: monster.passiveIds || []
  }));
  return {
    meta: generatedSource({
      schemaVersion: bundle.schemaVersion,
      generatedAt: bundle.generatedAt,
      currentSeason: bundle.currentSeason
    }),
    monsters: generatedSource(bundle.monsters),
    monsterSummaries: generatedSource(monsterSummaries),
    skills: generatedSource(bundle.skills),
    passives: generatedSource(bundle.passives)
  };
}

function main() {
  const bundle = validateBundle(JSON.parse(fs.readFileSync(sourcePath, "utf8")));
  const nextOutputs = generatedOutputs(bundle);
  const checkOnly = process.argv.includes("--check");

  if (checkOnly) {
    for (const [key, next] of Object.entries(nextOutputs)) {
      const outputPath = outputPaths[key];
      const current = fs.existsSync(outputPath)
        ? fs.readFileSync(outputPath, "utf8")
        : "";
      if (current !== next) {
        console.error("Mini Program data is out of date. Run node scripts/sync-miniprogram-data.js");
        process.exitCode = 1;
        return;
      }
    }
    console.log("Mini Program data is synchronized.");
    return;
  }

  fs.mkdirSync(outputDir, { recursive: true });
  for (const [key, next] of Object.entries(nextOutputs)) {
    fs.writeFileSync(outputPaths[key], next, "utf8");
  }
  const oldBundlePath = path.join(outputDir, "local-bundle.js");
  if (fs.existsSync(oldBundlePath)) fs.unlinkSync(oldBundlePath);
  console.log(
    "Generated Mini Program split data modules: "
    + `${bundle.monsters.length} monsters, ${bundle.skills.length} skills, `
    + `${bundle.passives.length} passives.`
  );
}

main();
